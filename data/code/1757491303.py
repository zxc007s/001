import cv2
import numpy as np

# 创建一个白色背景的空白图像
img = np.ones((500, 700, 3), dtype=np.uint8) * 255

# 绘制老虎的身体（橙色矫圈）
cv2.ellipse(img, (350, 300), (180, 100), 0, 0, 360, (0, 165, 255), -1)

# 绘制老虎的头部（圆形）
cv2.circle(img, (350, 180), 80, (0, 165, 255), -1)

# 绘制耳朵（两个小圆）
cv2.circle(img, (310, 130), 30, (0, 165, 255), -1)
cv2.circle(img, (390, 130), 30, (0, 165, 255), -1)

# 绘制眼睛（两个黑色小圆）
cv2.circle(img, (330, 170), 10, (0, 0, 0), -1)
cv2.circle(img, (370, 170), 10, (0, 0, 0), -1)

# 绘制鼻子（黑色小矫圆）
cv2.ellipse(img, (350, 200), (15, 10), 0, 0, 360, (0, 0, 0), -1)

# 绘制胸口（白色矫圆）
cv2.ellipse(img, (350, 250), (40, 30), 0, 0, 360, (255, 255, 255), -1)

# 绘制黑色斑纹（多条线条）
cv2.line(img, (300, 220), (280, 240), (0, 0, 0), 3)
cv2.line(img, (400, 220), (420, 240), (0, 0, 0), 3)
cv2.line(img, (320, 280), (300, 300), (0, 0, 0), 3)
cv2.line(img, (380, 280), (400, 300), (0, 0, 0), 3)

# 绘制尾巴（曲线，用多条线段模拟）
points = np.array([[180, 280], [160, 260], [140, 240], [120, 220]], np.int32)
points = points.reshape((-1, 1, 2))
cv2.polylines(img, [points], False, (0, 165, 255), 5)

# 绘制四条腿（四个矩形）
cv2.rectangle(img, (270, 350), (290, 450), (0, 165, 255), -1)
cv2.rectangle(img, (410, 350), (430, 450), (0, 165, 255), -1)
cv2.rectangle(img, (230, 320), (250, 420), (0, 165, 255), -1)
cv2.rectangle(img, (450, 320), (470, 420), (0, 165, 255), -1)

# 显示图像
cv2.imshow('Little Tiger', img)
cv2.waitKey(0)
cv2.destroyAllWindows()