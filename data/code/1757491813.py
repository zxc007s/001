import cv2
import numpy as np

# 创建白色背景图像
img = np.ones((500, 700, 3), dtype=np.uint8) * 255

# 绘制身体（橙色矩形）
cv2.rectangle(img, (200, 250), (500, 400), (0, 165, 255), -1)

# 绘制头部（橙色圆形）
cv2.circle(img, (350, 200), 80, (0, 165, 255), -1)

# 绘制耳朵（两个小橙色圆形）
cv2.circle(img, (300, 140), 30, (0, 165, 255), -1)
cv2.circle(img, (400, 140), 30, (0, 165, 255), -1)

# 绘制眼睛（两个黑色圆形）
cv2.circle(img, (320, 190), 15, (0, 0, 0), -1)
cv2.circle(img, (380, 190), 15, (0, 0, 0), -1)

# 绘制鼻子（黑色小圆形）
cv2.circle(img, (350, 220), 10, (0, 0, 0), -1)

# 绘制嘴巴（黑色线条）
cv2.line(img, (350, 230), (340, 250), (0, 0, 0), 3)
cv2.line(img, (350, 230), (360, 250), (0, 0, 0), 3)

# 绘制胡须（多条黑色线条）
cv2.line(img, (340, 235), (300, 240), (0, 0, 0), 2)
cv2.line(img, (340, 245), (300, 250), (0, 0, 0), 2)
cv2.line(img, (360, 235), (400, 240), (0, 0, 0), 2)
cv2.line(img, (360, 245), (400, 250), (0, 0, 0), 2)

# 绘制纹条（多条黑色矩形）
cv2.rectangle(img, (250, 260), (270, 390), (0, 0, 0), -1)
cv2.rectangle(img, (320, 260), (340, 390), (0, 0, 0), -1)
cv2.rectangle(img, (390, 260), (410, 390), (0, 0, 0), -1)
cv2.rectangle(img, (460, 260), (480, 390), (0, 0, 0), -1)

# 绘制尾巴（黑色线条）
cv2.line(img, (500, 320), (550, 280), (0, 0, 0), 8)

# 显示图像
cv2.imshow('Simple Tiger', img)
cv2.waitKey(0)
cv2.destroyAllWindows()